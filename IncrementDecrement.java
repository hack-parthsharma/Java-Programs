
public class IncrementDecrementExample1
{

	public static void main(String[] args)
	{
		//Pre Increment
		//pre(++a) means increment first use later
		int a,b;
	    a=5;
	    b=++a;
       System.out.println(b);
       //Post Increment
       //post(c++) means use first increment later
       int c,d;
	    c=5;
	    c++;
	    d=c++;
      System.out.println(d);
	}

}
